package lab_19_Junit_MathOperation;// User Defined Package

public class MathOperations {// Class Decalration
	// method to  Add 
	public int add(int a, int b)
	{
		return(a+b);
	}
	// method to Subtract
	public int sub(int a, int b)
	{
		return(a-b);
	}
	// method to Multiply
	public int multiply(int a, int b)
	{
		return(a*b);
	}
	// Method for Division
	public int div(int a, int b)
	{
		return(a/b);
	}

}
